package com.icraft.day6;

public class Relational_operatorsl {
     public static void main(String as[]) {
    	 int a, b;
    	 a = 40;
    	 b = 30;
    	 System.out.println("a == b = " + (a == b));
    	 System.out.println("a != b = " + (a != b));
    	 System.out.println("a > b = " + (a > b));
    	 System.out.println("a < b = " + (a < b));
    	 System.out.println("b >= a = " + (b >= a));
    	 System.out.println("b <= a = " + (b <= a));
     }
}
