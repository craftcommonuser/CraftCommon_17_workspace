package EncapsulationDemo1;

public class getterStudent {
	// all variables must be private
	private String firstName = "Jamie";
	private String lastName = "Foxx";
	private int age = 45;
	private String Collage = "Natural Science";
	
	
	public String getFirstName() {
		return firstName;
	}
	public String getLastName() {
		return lastName;
	}
	public int getAge() {
		return age;
	}
	public String getCollage() {
		return Collage;
	}
	
}
